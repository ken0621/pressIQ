	$('.autoplay').slick({
		slidesToShow: 4,
		slidesToScroll: 1,
		dots: false,
		autoplay: true,
		autoplaySpeed: 2000,
	});

	/*scroll up*/
	$(window).scroll(function () {
        if ($(this).scrollTop() > 700) {
            $('.scroll-up').fadeIn();
        } else {
            $('.scroll-up').fadeOut();
        }
    });

    $('.scroll-up').click(function () {
        $("html, body").animate({
            scrollTop: 0
        }, 700);
        return false;
    });
});
