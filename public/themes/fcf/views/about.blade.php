					<p>
						

					The work done by FCF Minerals Corporation at the Runruno project has attracted series of awards and recognition both internationally and nationally.<br><br>

					<p style="font-family: 'Fjalla One', sans-serif; font-size: 20px;">International Awards</p>

					<p style="font-weight: 700;">Mining Journal Outstanding Achievement Award:</p>

					The Mining Journal 2007 Outstanding Achievement Award was won by Dr. Ernesto Mendoza, FCF’s Senior Vice President, in awards ceremonies held in London for “remarkable work in assisting the community of Runruno, Quezon in Nueva Viscaya, Philippines in recovery from the effects of Super Typhoon “Paeng” in November 2006. This award is given to those who have exhibited long time charity work and for championing the mining industry. Dr. Mendoza is the first Filipino to win the award.<br><br>

					<p style="font-weight: 700;">Best Community Development Initiative Award (Asia Mining Congress – Singapore)</p>

					FCF received the award during the Asia Mining Congress Sustainability Awards 2009 at Singapore last March 25, 2009. FCF Minerals won by organizing a community development initiative that truly embodies what a community development project should be. The initiative is called Out-of-School Youth (OSY) Skills Development and Placement Program which was conceived in the light of the current economic climate that saw many overseas Filipino workers (OFWs) lose their jobs abroad. To date, 640 OSYs (from various mountain ethnic tribes) have received training and employment through the FCF program. The program was judged as strategic, relevant, sustainable and with broad-based participation from the concerned stakeholders.<br><br>

					<p style="font-family: 'Fjalla One', sans-serif; font-size: 20px;">National Awards</p>

					<p style="font-weight: 700;">Presidential Mineral Industry Enviromental Award (PMIEA)</p>

					For two years in a row FCF was awarded the PMIEA for its active role as a responsible mining company. It received its first PMIEA in 2011, the year when FCF bagged 3 of the top awards in the Exploration Category at the 58th Annual National Mine Safety and Environment Conference (ANMSEC) held yearly in Baguio City. The second PMIEA was given in 2012. The PMIEA award was created through Executive Order No. 374 “to promote the efficient management of the environment and ecology in the exploration, development and utilization of the country’s mineral resources.”<br><br>

					<p style="font-weight: 700;">Safest Mines Award – Exploration Category</p>

					In 2011, FCF Minerals Corporation also won the Safest Mines Awards – Exploration Category “A” after it achieved its 2-Million Manhours Without a Lost Time Accident and for keeping exploration activities free from incidents that could have affected work time during its exploration activities.<br><br>

					<p style="font-weight: 700;">Mining Forest Award</p>
					Also in 2012 and 2011, FCF Minerals Corporation won the 1st Runner Up (Exploration Category) recognition in the Best Mining Forest that acknowleged the company’s successful implementation of its Integrated Mining Reforestation Program. Since 2008, the company has been consistently winning this accolade. The award was given based primarily on nursery operations, extent of area planted, protection and maintenance within the exploration area.<br><br>

					<p style="font-weight: 700;">Platinum Award</p>
					FCF Minerals also won the Platinum Award (Exploration Category) for exemplary performance in environmental management, which was awarded during the same event (ANMSEC).

					<p style="font-weight: 700;">The Kabalikat Award</p>

					The Kabalikat Award was presented to FCF Minerals Corporation by the Office of the President of the Philippines, through the Technical Education and Skills Development Authority (TESDA) as a Regional Winner (Region 2) in August 2008. FCF Minerals Corporation was selected as a winner because it was recognized for outstanding contributions to the promotion and enhancement of technical education and skills development. FCF was honoured as an industry partner for focusing in the promotion and development of the country’s middle level manpower.

