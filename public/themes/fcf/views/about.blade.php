					<p>
						The Runruno project has a very strong community development component with a continuing commitment to behave ethically and contribute to local economic development, while improving the quality of life of its stakeholders and the society at large.<br><br>

						The implementation of the CSR program goes beyond the statutory obligations laid down in the Philippine Mining Act of 1995 and is a voluntary gesture of a company endeavouring to become a positive neighbour. To implement this philosophy, FCF Minerals established the Runruno Livelihood Foundation (The Foundation or RLF) to be its community development arm, which was tasked to establish a strong and lasting relationship with local stakeholders while at the same time fulfilling its commitments to the host communities.<br><br>

						The primary rationale for setting up the Foundation was to separate community development efforts from exploration and mining operations. The Foundation has an entirely separate budget and organization. The Foundation has 11 regular staff and 14 volunteer workers from different disciplines. It is headed by Dr Ernesto Mendoza, a senior executive with more than 35 years experience in managing CSR programs. Ernesto was awarded the Mining Journal’s 2007 Outstanding Achievement award in London for “remarkable work done in the host community of Runruno during the aftermath of Typhoon Paeng” in November of 2006 which buried most parts of the village with a mudslide and where several residents lost their lives. Very recently, FCF community development programs earned further recognition at the Asia Mining Congress in Singapore by winning the Best Community Development Initiative Award, beating other companies including Centerra Gold and PT Freeport of Indonesia.<br><br>

						The Foundation focuses on empowering people so that they are able to help themselves, and supports local improvement programs in education and skills development, health care, sanitation, employment, environmental awareness and literacy. These projects are jointly financed by FCF Minerals in partnership with various local government agencies and managed by a dedicated team of local people. It was officially registered with the Philippine Securities and Exchange Commission (SEC) on August 2006 under SEC Registration No. CN200612122.<br><br>

