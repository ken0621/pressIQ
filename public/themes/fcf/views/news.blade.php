						<p>
							FCF Minerals’ first baccalaureate scholars finally marched to receive their respective diplomas and medals last March 2013. Both finished their geo-sciences courses from Adamson University in Manila.<br><br>

							Wesley Chan graduated magna cum laude under Bachelor of Science in Mining Engineering,  while Vyron Leal, who took up Bachelor of Science in Geology, was among the top ten students in his class. They each had to maintain an average grade of 85% in order to stay in the scholarship program.<br><br>

							The two are the pioneering beneficiaries of the company’s scholarship program. FCF Community Relations Manager Melanie Valenciano said, “We are so proud of Byron and Wesley. They did not just  lived up to our expectations, they even surpassed it.”<br><br>

							The college scholarship was an offshoot of the original Out-of-School Youth Skills Development and Placement Program of FCF that sponsored out-of-school youths in skills training to eventual employment. This program won the company an international award in 2009.<br><br>

							FCF provided full-scholarship to Wesley and Vyron which included free tuition and a monthly allowance of P4,000 in cash. FCF also paid for their board and lodging, transportation, uniform, books, school projects and educational fieldtrips. The company also provided cash allowances when they took their On-the-Job-Training at Philex Mines during the summer of last year. Likewise, they will receive an allowance for their board exam, plus two years  of employment in the company whither or not they pass the board exam.<br><br>

							Wesley was a high school valedictorian in St. Mary’s College, Bayombong and Vyron finished high school with 3rd Honors distinction from Aldersgate College in Solano.<br><br>

							The scholarship committee was composed of representatives from the academe; provincial government of Nueva Vizcaya in the person of Honorable Vice Governor Jose Gene Gambito; religious sector; the Nueva Vizcaya Press Club; and TESDA as represented by Ms. Eva Tolentino who monitored each student and regularly submit report to the company.<br><br>

							The road to success was not without curves and bumps for Vyron and Wesley.<br><br>

							On his first week in Manila, Vyron had to change boarding houses several times because the rooms were mostly hot and uncomfortable that it triggered his asthma. He also experienced severe home sickness and wanted to go home during his first few weeks away from his family. However, his determination to finish his education helped him cope and guided him right to the finish line.<br><br>

							Wesley, on the other hand, was not even planning on pursuing  the course of Mining Engineering.  He wanted to take up BS Nursing originally, but when he heard about the FCF scholarship program he reckoned he would try it out. When he was chosen as one of the three scholars he was determined to prove his worth and so he put so much pressure on himself  to do well in his academic studies. This self-imposed pressure forced him to neglect his family and friends. For the first three years in college he never had friends and missed a lof of family affairs. He eventually came to and started to balance his education with social life.<br><br>

							The two graduates expressed gratitude towards FCF, “I’m so grateful to all the people behind (FCF) for providing us five years of college education…We’re looking forward to being part of the FCF working family soon,” says Wesley. They likewise pledged to pay the goodwill forward by becoming advocates of responsible mining and getting scholars of their own in the future.<br><br>

							Asked whether he regretted taking a career in the mineral industry Wesley said, “No regret at all. In fact, I have decided to become an advocate of responsible mining.”
